import { Request, Response } from "express";
import { CustomError, RoleUserDto } from "../../domain";
import { RoleUserService } from "../services/role_user.service";

export class RoleUserController {

    protected roleUserService: RoleUserService = new RoleUserService();

    private handleError(error: unknown, res: Response) {
        if (error instanceof CustomError) {
            return res.status(error.statusCode).json({ message: error.message });
        }
        console.log(error);
        res.status(500).json({ message: 'Internal server error' });
    }

    // MÃ©todos de la clase
    getAll = async (req: Request, res: Response) => {
        this.roleUserService.getRolesUsers()
            .then((data) => {
                res.json(data);
            })
            .catch((error) => {
                this.handleError(error, res);
            });
    }

    getById = async (req: Request, res: Response) => {
        const id = req.params.id;
        if (!id) return res.status(400).json({ message: 'Missing id' });

        this.roleUserService.getRoleUser(parseInt(id))
            .then((data) => {
                res.json(data);
            })
            .catch((error) => {
                this.handleError(error, res);
            });
    }

    create = async (req: Request, res: Response) => {
        const [error, createDto] = RoleUserDto.create(req.body);
        if (error) return res.status(400).json({ message: error });

        this.roleUserService.createRoleUser(createDto!)
            .then((data) => {
                res.json(data);
            })
            .catch((error) => {
                this.handleError(error, res);
            });
    }

    delete = async (req: Request, res: Response) => {
        const id = req.params.id;
        if (!id) return res.status(400).json({ message: 'Missing id' });

        this.roleUserService.deleteRoleUser(parseInt(id))
            .then((data) => {
                res.json(data);
            })
            .catch((error) => {
                this.handleError(error, res);
            });
    }

}